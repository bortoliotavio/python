import math
x = 1.1234

print('round => {}'.format(round(x)))
print('round com duas casa decimais => {}'.format(round(x, 2)))
print('ceil => {}'.format(math.ceil(x)))
print('floor => {}'.format(math.floor(x)))
print('Trunc = > {}'.format(math.trunc(x)))
print('Sqrt => {}'.format(math.sqrt(x)))
